import numpy as np
a = np.array([6,8,10])         
# b = np.array([2,3,4]) 

# mean()
print(np.mean(a))

# median()
print(np.median(a))

# var()
print(np.var(a))

# std()
print(np.std(a))

# min()
print(np.min(a))

# max()
print(np.max(a))

# sum()
print(np.sum(a))

# prod()
print(np.prod(a))

# argmin()
print(np.argmin(a))

# argmax()
print(np.argmax(a))

